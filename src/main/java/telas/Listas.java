    private void jListListasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListListasMouseClicked
        if (evt.getClickCount() == 2) {

        int index = jListListas.getSelectedIndex();

        if (index != -1) {
            Lista listaSelecionada = listasBanco.get(index);

            new TelaLista(listaSelecionada).setVisible(true);
        }
    }
    }//GEN-LAST:event_jListListasMouseClicked

