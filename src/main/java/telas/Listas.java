    private void jbAdicionarListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAdicionarListaActionPerformed
         String nomeLista = javax.swing.JOptionPane.showInputDialog(
            this,
            "Digite o nome da lista:"
    );

    if (nomeLista != null && !nomeLista.trim().isEmpty()) {

    Lista novaLista = new Lista(0, nomeLista);

        ListaDAO dao = new ListaDAO();

        dao.salvar(novaLista);

        modeloListas.addElement(nomeLista);

    javax.swing.JOptionPane.showMessageDialog(
            this,
            "Lista criada com sucesso!"
    );
}
    }//GEN-LAST:event_jbAdicionarListaActionPerformed

