    public TelaLista(Lista lista) {
        initComponents();

        tabela.setDefaultEditor(Object.class, null);

        this.lista = lista;

        ListaItemDAO listaItemDAO = new ListaItemDAO();

        this.lista.getItens().addAll(
                listaItemDAO.listarItensDaLista(lista.getId())
        );

        model = (DefaultTableModel) tabela.getModel();

        carregarItens();
    }

private void carregarItens() {

    model.setRowCount(0);

    lista.getItens().clear();

    ListaItemDAO dao = new ListaItemDAO();

    lista.getItens().addAll(
        dao.listarItensDaLista(lista.getId())
    );

    for (Item i : lista.getItens()) {

        model.addRow(new Object[]{
            i.getNome(),
            i.getMarcaFornecedor(),
            i.getQuantidade(),
            i.isComprado() ? "Comprado" : "Pendente"
        });
    }
}



private void adicionarItemLista() {

    ItemDAO itemDAO = new ItemDAO();

    ArrayList<Item> itensBanco = itemDAO.listar();

    if (itensBanco.isEmpty()) {

        JOptionPane.showMessageDialog(
                this,
                "Nenhum item cadastrado!"
        );

        return;
    }

    String[] nomesItens = new String[itensBanco.size()];

    for (int i = 0; i < nomesItens.length; i++) {

        nomesItens[i] =
                itensBanco.get(i).getNome();
    }

    String escolhido = (String) JOptionPane.showInputDialog(
            this,
            "Escolha um item:",
            "Adicionar Item",
            JOptionPane.PLAIN_MESSAGE,
            null,
            nomesItens,
            nomesItens[0]
    );

    if (escolhido != null) {

        for (Item item : itensBanco) {

            if (item.getNome().equals(escolhido)) {

                String qtdTexto =
                        JOptionPane.showInputDialog(
                                this,
                                "Digite a quantidade:"
                        );

                try {

                    int qtd = Integer.parseInt(qtdTexto);

                    Item copia = new Item(
                            item.getId(),
                            item.getNome(),
                            qtd,
                            item.getMarcaFornecedor()
                    );

                    lista.adicionarItem(copia);

                    ListaItemDAO listaItemDAO =
                            new ListaItemDAO();

                    listaItemDAO.salvar(
                            lista.getId(),
                            item.getId(),
                            qtd
                    );

                } catch (NumberFormatException e) {

                    JOptionPane.showMessageDialog(
                            this,
                            "Digite um número válido!"
                    );
                }

                break;
            }
        }

        carregarItens();
    }
}


